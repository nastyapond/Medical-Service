import random

urgency_map = {
    "consult": "Консультационное",
    "plan": "Плановое",
    "urgent": "Срочное",
    "emergency": "Экстренное"
}

types_map = {
    "consult": ["Консультация или вопрос", "Наблюдение"],
    "plan": ["Запись на прием", "Наблюдение"],
    "urgent": ["Вызов врача", "Консультация или вопрос"],
    "emergency": ["Перенаправление в экстренные службы", "Вызов врача"]
}

templates = {
    "consult": [
        "Температура 36.6, это нормально?",
        "Подскажите, что делать при легкой головной боли?",
        "Можно ли принимать это лекарство?",
        "Какой врач лечит такие симптомы?",
        "Нужно ли идти к врачу при усталости?",
        "Есть ли противопоказания у этого препарата?",
        "Нужно ли сдавать анализы?"
    ],
    "plan": [
        "Хочу записаться к терапевту",
        "Нужно пройти плановый осмотр",
        "Запишите меня к врачу на этой неделе",
        "Хочу пройти диспансеризацию",
        "Нужна справка для работы",
        "Хочу записаться к кардиологу",
        "Нужно обследование"
    ],
    "urgent": [
        "Температура 39 уже второй день",
        "Сильная боль в животе",
        "Головокружение и слабость",
        "Резкая боль в спине",
        "Болит ухо у ребенка",
        "Сильная головная боль",
        "Плохо себя чувствую сегодня"
    ],
    "emergency": [
        "Сильная боль в груди и трудно дышать",
        "Потеря сознания",
        "Не могу дышать",
        "Сильное кровотечение",
        "Острая боль в сердце",
        "Судороги и потеря ориентации",
        "Очень сильная боль после травмы"
    ]
}

def generate_dataset(n=5000):
    rows = []
    for _ in range(n):
        key = random.choice(list(templates.keys()))
        text = random.choice(templates[key])
        urgency = urgency_map[key]
        req_type = random.choice(types_map[key])

        rows.append(f"{text},{urgency},{req_type}")
    
    return rows

data = generate_dataset(5000)

with open("medical_dataset_5000.txt", "w", encoding="utf-8") as f:
    f.write("\n".join(data))

print("Готово: medical_dataset_5000.txt")